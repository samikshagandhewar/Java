package com.studyopedia;

public class unary {

	public static void main(String[] args) {
		int a= 244;
		int b= 855;
		
		System.out.println(a);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(b);
		System.out.println(b++);
		System.out.println(++b);
		System.out.println(b);

	}

}
